/**
 * Resolve tokens flat para o tema atual (`dark`/`light`).
 * Usado pelo SettingsProvider quando aplica overrides.
 */
export function resolvePresetTokens(
  presetId: string | undefined,
  themeMode: 'dark' | 'light',
): Record<string, string> {
  if (!presetId) return {};
  const preset = THEME_PRESETS.find((p) => p.id === presetId);
  if (!preset) return {};
  return preset.tokens[themeMode] ?? {};
}
